import Foundation


//String
let nombre: String = "Alexander"
let apellido = "Guitz"
let nombreCompleto = nombre + " " + apellido
print(nombreCompleto)

//Int
let myInt: Int = 1
let myInt2 = 2
print(myInt + myInt2)

//Double

let myDouble: Double = 1.5
let mydouble2 = 2.6
print(myDouble + mydouble2)

//Float ( Decimales pequenos , para decimales largos usar Double )

let myFloat: Float = 1.5
let myfloat2: Float = 2.6
print(myFloat + myfloat2)

//Bool

let myBool = true
let myBool2 = false
print(myBool && myBool2)
print(myBool == myBool2)
